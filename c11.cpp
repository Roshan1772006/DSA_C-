#include<iostream>
using namespace std;
int main(){
        // x++ post increment
        // pehle use karo then increase
        int x = 10;
        cout<< x++<<endl;
        cout<<x<<endl;

        //++x pre increment
        // pehle increase then use
        cout<<++x<<endl;
        cout<<x<<endl;

        //x-- post decrement
        // pehle use karo then decrease
        cout<< x-- <<endl;
        cout<<x<<endl;

        //--x pre decrement
        // pehle decrease then use
        cout<< --x <<endl;
        cout<<x<<endl;
}